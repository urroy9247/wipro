n=[50,70,40,80,45]
r=0
c=[]
d=[]
p=0
 
for i in n:
    if i>=50:
        r+=1
        c.append(i)
    else:
        p+=1
        d.append(i)
print(f"more than 50 is {r} and the numbers are {c}")
print(f"less than 50 is {p} and the numbers are {d}")




























































































































































































































































































































































































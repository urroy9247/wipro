#Create a dictionary by extracting the keys from a given dictionary
sample_dict = {
    "name": "Kelly",
    "age": 25,
    "salary": 8000,
    "city": "New york"}

# Keys to extract
keys = ["name", "salary"]
new_dict = {}
for i in keys:
    if i in sample_dict:
        new_dict[i] = sample_dict[i]
print(new_dict)


